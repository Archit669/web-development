// push
let fruits = ["apple","mango","guava"];
fruits.push("grapes");
console.log(fruits);
// pop will return pooped item(last element)
let popped_item = fruits.pop();
console.log(fruits);
console.log(popped_item);
// unshift will add element in start.
fruits.unshift("watermelon");
console.log(fruits);
// shift will remove element from start and return it.
popped_item = fruits.shift();
console.log(fruits);
console.log(popped_item);

