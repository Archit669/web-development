// rules for naming variables

// 1. you cannot start with numbers
// var 1name = "archit"; // cause error
// var name1 = "archit"; // not cause error




// 2. you can only use _(underscore) and $(dollar) symbol.

// using underscore sign
var first_name = "archit";   // snake case writing
console.log(first_name);
var firstName = "archit";    // camel case writing
console.log(firstName);

// using dollar sign
var first$name = "mohit";
var $firstname = "mohit";




// 3. you cannot use spaces
// var first name = archit;  // will cause error




/*convention
start with small letters and use camelcase*/



